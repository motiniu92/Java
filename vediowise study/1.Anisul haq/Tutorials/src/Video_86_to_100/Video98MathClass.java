public class Video98MathClass {
    public static void main(String[] args) {
       //Math class
        double x = Math.abs(-9.5);
        System.out.println("Absolute value: "+ x);
        System.out.println("Direct Print of Absolute value: "+ Math.abs(-12.34));
        //Square root
        System.out.println("Square root: "+ Math.sqrt(102));
        //power
        System.out.println("Power: "+ Math.pow(2,3));//Always returns double value
       //PI
        System.out.println("Pi: "+ Math.PI);
       //log
        System.out.println("Log: " + Math.log(2.0));
       //exponential
        System.out.println("Exponential: "+ Math.exp(10));
        //Max,Min
        System.out.println("Max: "+ Math.max(1.0, 45.43));
        System.out.println("Min: "+ Math.min(34.3, 3));
        //ceil
        System.out.println("Ceil: "+ Math.ceil(-5.1)); // -5
        System.out.println("Floor: "+ Math.floor(-5.1)); //-6
        System.out.println("Ceil: "+ Math.ceil(5.6)); // -5
        System.out.println("Floor: "+ Math.floor(5.6)); //-6
        
        
       
    }
}
