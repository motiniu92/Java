package com.coderbd;

public class MathClassmethodsEx {

    public static void main(String[] args) {
        System.out.println(Math.toDegrees(Math.PI / 2));
        System.out.println(Math.toRadians(90));
        System.out.println(Math.sqrt(100));
        System.out.println(Math.ceil(2.1));
        System.out.println(Math.rint(2.1575555555555));
        System.out.println(Math.rint(2.1600000000000));
        System.out.println(Math.max(2.16, 2.15555555));
        System.out.println(Math.min(2.3, 2.21));
    }

}
